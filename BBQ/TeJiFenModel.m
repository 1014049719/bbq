#import "TeJiFenModel.h"

@implementation TeJiFenModel

- (instancetype)initWithDic:(NSDictionary *)dic {

  if (self = [super init]) {
    self.giftName = [NSString stringWithFormat:@"%@", dic[@"giftname"]];
    self.giftID = [NSString stringWithFormat:@"%@", dic[@"id"]];
    self.imgUrl = [NSString stringWithFormat:@"%@", dic[@"imgurl"]];
    self.giftNum = [NSString stringWithFormat:@"%@", dic[@"giftnum"]];
    self.flag = [NSString stringWithFormat:@"%@", dic[@"flag"]];
    self.jiFen = [NSString stringWithFormat:@"%@", dic[@"jifen"]];
  }
  return self;
}

@end
